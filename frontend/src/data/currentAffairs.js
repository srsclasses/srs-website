// Utility functions
export const getCurrentAffairsByDate = (dateString) => {
  return currentAffairsData.find(data => data.date === dateString);
};

export const getAllDatesWithCurrentAffairs = () => {
  return currentAffairsData.map(data => data.date).sort((a, b) => new Date(b) - new Date(a));
};

export const getLatestCurrentAffairs = () => {
  const sortedData = [...currentAffairsData].sort((a, b) => new Date(b.date) - new Date(a.date));
  return sortedData[0];
};

export const getCurrentAffairsByCategory = (dateString, category) => {
  const data = getCurrentAffairsByDate(dateString);
  if (!data) return [];
  
  if (category === 'national') return data.national;
  if (category === 'international') return data.international;
  
  // For state category, return first available state's news
  if (category === 'state' && data.state) {
    const states = Object.keys(data.state);
    return states.length > 0 ? data.state[states[0]] : [];
  }
  
  return [];
};

export const getStateNews = (dateString, stateName = 'Bihar') => {
  const data = getCurrentAffairsByDate(dateString);
  if (!data || !data.state) return [];
  return data.state[stateName] || [];
};

export const getAvailableStates = (dateString) => {
  const data = getCurrentAffairsByDate(dateString);
  if (!data || !data.state) return [];
  return Object.keys(data.state);
};

export const searchCurrentAffairs = (query) => {
  const results = [];
  currentAffairsData.forEach(dateData => {
    const national = dateData.national.filter(item => 
      item.title.toLowerCase().includes(query.toLowerCase()) ||
      item.summary.toLowerCase().includes(query.toLowerCase()) ||
      item.tags.some(tag => tag.toLowerCase().includes(query.toLowerCase()))
    );
    const international = dateData.international.filter(item => 
      item.title.toLowerCase().includes(query.toLowerCase()) ||
      item.summary.toLowerCase().includes(query.toLowerCase()) ||
      item.tags.some(tag => tag.toLowerCase().includes(query.toLowerCase()))
    );
    
    let stateNews = [];
    if (dateData.state) {
      Object.keys(dateData.state).forEach(stateName => {
        const filtered = dateData.state[stateName].filter(item =>
          item.title.toLowerCase().includes(query.toLowerCase()) ||
          item.summary.toLowerCase().includes(query.toLowerCase()) ||
          item.tags.some(tag => tag.toLowerCase().includes(query.toLowerCase()))
        );
        stateNews = [...stateNews, ...filtered];
      });
    }
    
    if (national.length > 0 || international.length > 0 || stateNews.length > 0) {
      results.push({
        date: dateData.date,
        national,
        international,
        state: stateNews
      });
    }
  });
  return results;
};
